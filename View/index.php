        <h2>Group'IMAC ? C'est quoi ?</h2>

        <p>A l’IMAC, les travaux de groupe sont légion, et il est parfois difficile de trouver des partenaires de projet, surtout dans ce contexte difficile. En effet, les interactions entre les étudiants sont réduites du fait des cours à distance et du peu de présence sur le campus, et ils n’ont pas forcément eu le temps de tisser des liens avec leurs camarades de promotion. On se connaît moins, on évalue moins facilement les compétences des autres… Ainsi, la formation de groupe devient ardue. C’est pour ces raisons que nous avons décidé de créer Group’IMAC ! </p>

        <p>Ce site a pour vocation de fonctionner à la façon d’un panneau de “petites annonces”. Un projet à réaliser, mais personne d’autre pour le faire ? Il suffira de poster une annonce détaillant l’idée, les membres du groupe actuel s’il y en a, la catégorie, le contexte (personnel, professionnel, scolaire) du projet… Tout le monde pourra consulter ces annonces, y répondre dans les commentaires, être ajouté aux équipes du projet… Ainsi, lancer un travail de groupe devient beaucoup plus facile avec Group’IMAC ! </p>

